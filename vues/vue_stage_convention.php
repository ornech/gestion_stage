  <h2>TITRE I : DISPOSITIONS GÉNÉRALES</h2>

  <p><strong>Article 1 (objet) :</strong></p>
  <p>La présente convention a pour objet la mise en œuvre, au bénéfice des étudiants du lycée, d’une
    action d’éducation concertée, organisée, conformément aux dispositions du décret n°2006-1093 du 29
    août 2006, modifié par le décret n°2010-956 du 25 août 2010, pris en application de l’article 9 de
    la loi n°2006-396 du 31 mars 2006 pour l’égalité des chances. Si le stage se déroule à l’étranger,
    la convention pourra être adaptée pour tenir compte des contraintes imposées par la législation du
    pays d’accueil.</p>

    <p><strong>Article 2 (programme) :</strong></p>
    <p>Les stages sont destinés à donner à l’étudiant une représentation concrète du milieu professionnel
      des services informatiques et de l’emploi, tout en lui permettant d’acquérir et d’éprouver les
      compétences professionnelles prévues par le référentiel. Le programme du stage est établi par le
      chef d’entreprise. Le contenu de ce projet est soumis à l’approbation de l’équipe pédagogique, en
      fonction du programme général des études et de la spécialisation du stagiaire.</p>
      <p>Le sujet proposé est obligatoirement décrit sommairement ci-après :</p>
      <p>En cas de besoin, il fait l’objet d’une annexe qui le décrit de façon détaillée.</p>

      <p><strong>Article 3 (durée) :</strong></p>
      <p>Le stage est fixé aux dates suivantes :</p>
      <p>du lundi 13 mai 2024 au vendredi 21 juin 2024 inclus.</p>

      <p><strong>Article 4 (statut du stagiaire) :</strong></p>
      <p>Le stagiaire, pendant la durée de son séjour en entreprise, conserve son statut d’étudiant. Il est
        suivi par un directeur de stage, en accord formel avec le chef d’entreprise d’accueil.</p>

        <p><strong>Article 5 (assiduité et discipline) :</strong></p>
        <p>Durant son stage, le stagiaire est soumis à la discipline de l’entreprise, notamment en ce qui
          concerne l’horaire. En cas de manquement à la discipline, le chef d’entreprise peut mettre fin au
          stage, après avoir prévenu le chef d’établissement. Avant son départ, le chef d’entreprise
          s’assurera que l’avertissement a bien été reçu par ce dernier, et, s’il s’agit d’un stagiaire logé
          par l’entreprise, que toutes dispositions ont été prises pour le recevoir.</p>

          <p><strong>Article 6 (accidents) :</strong></p>
          <p>Les étudiants bénéficient de la législation sur les accidents du travail, en application de l’article
            410, 2e,1er paragraphes du code de la Sécurité Sociale. <BR>
              Toutefois il leur est conseillé de contracter eux-mêmes, ou par l’intermédiaire de leur représentant légal, une assurance garantissant
              leur responsabilité civile pour tout dommage qu’ils pourraient causer à autrui de leur propre fait.<BR>
                En cas d’accident survenant à l’étudiant stagiaire, soit au cours du travail, soit au cours du
                trajet, le Chef d’Entreprise s’engage à faire parvenir toutes les déclarations, le plus rapidement
                possible à Monsieur le Proviseur ; il utilise à cet effet, les imprimés spéciaux mis à sa
                disposition par le Lycée. Le chef d’entreprise contractera une assurance, garantissant sa propre
                responsabilité civile, chaque fois qu’elle sera engagée.
              </p>

                <p><strong>Article 7 (rémunération) :</strong></p>
                <p>Le stage ne pourra être considéré comme une période d’activité salariée. Le stagiaire ne perçoit
                  aucune rémunération et est exclu du bénéfice des avantages sociaux et salariés. En cas d’engagement
                  ultérieur, la période du stage ne sera pas prise en compte au titre de l’ancienneté.</p>

                  <p><strong>Article 8 (avantages en nature) :</strong></p>
                  <p>L’ensemble des frais occasionnés, hors mission spécifique confiée au stagiaire par l’entreprise
                    pendant le déroulement de ce stage, reste à l’entière charge du stagiaire.</p>

                    <p><strong>Article 9 (Attestation) :</strong></p>
                    <p>En fin de stage, une attestation est remise au stagiaire par le responsable de l’organisation
                      d’accueil. <BR>Elle précise les dates, la durée et le contenu du stage. En fin de stage une attestation
                        de réalité des situations professionnelles rencontrées par le stagiaire doit être signée par le
                        tuteur du stagiaire. Cette attestation est réalisée par le stagiaire en conformité avec son
                        portefeuille de compétence.</p>

                        <p><strong>Article 10 (confidentialité) :</strong></p>
                        <p>Les étudiants stagiaires sont tenus à une obligation de discrétion absolue. A cet égard l’étudiant
                          s’engage à ne divulguer à qui que ce soit aucune information ou donnée à caractère confidentiel
                          qu’il sera en mesure de connaître lors de son stage. L’étudiant doit respecter les biens matériels
                          de l’entreprise en matière de logiciel, l’étudiant s’engage à ne commettre aucune infraction
                          informatique :</p>
                          <ul>
                            <li>Piratage de logiciel</li>
                            <li>Dégradation volontaire de données</li>
                            <li>Intrusion de virus</li>
                          </ul>
                          <p>En cas de non respect de l’une des obligations citées ci-dessus, le chef d’entreprise se réserve le
                            droit de mettre fin au stage de l’étudiant fautif après avoir informé le responsable pédagogique du
                            lycée.</p>

                            <p><strong>Article 11 (visite) :</strong></p>
                            <p>Une visite en entreprise sera réalisée par un enseignant durant la période de stage. Elle fera
                              l’objet d’un entretien entre le tuteur de stage et l’enseignant concerné.</p>


                              <h2>TITRE II : PROLONGATION</h2>


                              <p><strong>Article 1:</strong></p>
                              <p>L’étudiant en stage ne peut prétendre à aucune rémunération.</p>
                              <p>Toutefois, certaines entreprises accordent une gratification aux stagiaires en fonction du sérieux de leur travail et de la qualité des services rendus.</p>

                              <p><strong>Article2:</strong></p>
                              <p>L’étudiant est suivi durant son stage par un professeur. Une visite d’un enseignant aura lieu dans la deuxième
                                partie du stage et sera l’occasion de rencontrer le tuteur du stagiaire qui donnera son avis sur le déroulement
                                du stage et l’implication du stagiaire. En cas de stage éloigné, la visite peut être remplacée par un entretien
                                téléphonique. Le tuteur s’engage à communiquer le plus rapidement à l’enseignant responsable tout problème qui
                                se poserait durant la période de stage. </p>

